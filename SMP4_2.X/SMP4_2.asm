	LIST	P=PIC16F84A
	INCLUDE	"P16F84A.INC"
		
	__CONFIG    _HS_OSC&_WDT_OFF
		
CNT1	EQU	    0CH
CNT2	EQU	    0DH
CNT3	EQU	    0EH
CNT4	EQU	    0FH
TMP	EQU	    10H

	ORG	    0H

MAIN	
	BSF	    STATUS , RP0
	CLRF	    TRISB
	BCF	    STATUS , RP0
	MOVLW	    B'10000000'
	MOVWF	    PORTB
	GOTO	    MAINLP
	
MAIN1
	CALL	    TIME125M
	BCF	    STATUS , C
	MOVLW	    B'10000000'
	MOVWF	    PORTB
	GOTO	    MAINLP
	
MAINLP	
	CALL	    TIME125M
	RRF	    PORTB,F
	BTFSC	    PORTB,0
	GOTO	    MAIN1
	GOTO	    MAINLP
	
TIME100U						
	MOVLW	    0A5H			
	MOVWF	    CNT1			
	NOP						
	NOP						
LOOP1
	DECFSZ	    CNT1,F			
	GOTO	    LOOP1			
	RETURN					
		

TIME10M
	MOVLW	    063H			
	MOVWF	    CNT2			
	NOP						
	NOP		
LOOP2
	CALL	    TIME100U
	DECFSZ	    CNT2,F	
	GOTO	    LOOP2			
	RETURN					
	
TIME125M
	MOVLW	    0cH			
	MOVWF	    CNT3
LOOP3
	CALL	    TIME10M
	DECFSZ	    CNT3,F
	GOTO	    LOOP3
	CALL	    TIME50M
	RETURN
	
TIME50M
	MOVLW	    032H			
	MOVWF	    CNT4
LOOP4
	CALL	    TIME100U
	DECFSZ	    CNT4,F
	GOTO	    LOOP4
	RETURN	
	END

